func Identul(size int) (r Matrixul) {
	r = make([]uint64, size*size) // Make automatically zeroes everything (like calloc), only need to set the 1's
	for i := 0; i < size; i++ {
		r[i*size+i] = 1 //i * size + 1 is the main diagonal for a matrix with number of columns <size>
	}

	return r
}

